package edu.brown.cs.student.main;

public class StarDistPair {
  private int id;
  private double dist;

  public StarDistPair(int id, double dist) {
    this.id = id;
    this.dist = dist;
  }

  public int getID() {
    return this.id;
  }

  public double getDist() {
    return this.dist;
  }
}
